# How to Check if a List is Empty in Python

# Solution1:Using Boolean opearator
my_list=[]
if not my_list:
    print("My list is empty")

#Solution 2:Using len() function
my_list=[]
if len(my_list)==0:
    print("The list is empty")

# Solution 3: Using []
my_list=[]
if my_list==[]:
    print("The list is empty")
